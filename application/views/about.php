<h2  style=" text-align: left; ">Understanding Complex Systems......one model at a time</h2>
<div  class="paragraph editable-text" style=" text-align: left; display: block; "><font size="3">

<p align="justify" class="style5">We computer scientists, with a long legacy in graph theory and algorithms, strongly feel that we have a lot to contribute to and learn from the very recent and extremely potent field of <em><b>Complex Networks</b></em>. This is the primary reason for the inception of the <strong>C</strong>omplex <strong>Ne</strong>tworks <strong>R</strong>esearch <strong>G</strong>roup (<strong>CNeRG</strong>) at the <a href="http://cse.iitkgp.ac.in/">Department of Computer Science and Engineering</a>, <a href="http://www.iitkgp.ac.in/">IIT Kharagpur</a>, India. Our main objective is to have a cross-fertilization of graph theory and complex networks and thereby, have a better understanding of the dynamics of various large-scale systems. Some of the problems that the group focuses on are related to:</p>

<p align="justify" class="style5">(a) Online social networks</p>

  <p align="justify" class="style5">(b) Peer-to-peer networks </p>

  <p align="justify" class="style5">(c) Delay tolerant networks</p>

  <p align="justify" class="style5"> (d) Linguistic networks</p>

  <p align="justify" class="style5">Apart from pursuing cutting-edge research, the group has many other activities such as (i) holding regular meetings of the group-members often accompanied by various international researchers (ii) offering a regular "complex network theory" course in the Department (iii) conducting seminars and workshops in and outside the Institute with a primary focus on complex networks (iv) fostering various national and international research collaborations (with both universities as well as research labs) (v) carrying out several funded projects and (vi) conducting tutorials in India and abroad to make people (and especially computer scientists) aware of this interesting as well as important area of research.&nbsp;</p>

